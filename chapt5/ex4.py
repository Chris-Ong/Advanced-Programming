import math

def main():
    a = 2.3
    ceil_of_a = math.ceil(a)
    floor_of_a = math.floor(a)
    factorial_of_a = math.factorial(5)
    power_of_23 = math.pow(2, 3)
    sqrt_of_a = math.sqrt(16)

    print(f"Ceil of {a}: {ceil_of_a}")
    print(f"Floor of {a}: {floor_of_a}")
    print(f"Factorial of 5: {factorial_of_a}")
    print(f"Value of 2^3: {power_of_23}")
    print(f"Square root of 16: {sqrt_of_a}")

if __name__ == "__main__":
    main()
